BEGIN

	-- BASKET
	UPDATE "BASKET" b SET (b."INVOICETOADDRESSURN", b."SHIPTOADDRESSURN", b."SHIPFROMADDRESSURN", b."SERVICETOADDRESSURN", b."INSTALLTOADDRESSURN") = (
		SELECT
			NVL((SELECT 'urn:address:basket:' || ba."BASKETID" || ':' || ba."UUID" FROM "BASKETADDRESS" ba WHERE b."INVOICETOADDRESSURN" = ba."UUID"),  b."INVOICETOADDRESSURN"),
			NVL((SELECT 'urn:address:basket:' || ba."BASKETID" || ':' || ba."UUID" FROM "BASKETADDRESS" ba WHERE b."SHIPTOADDRESSURN" = ba."UUID"), b."SHIPTOADDRESSURN"),
			NVL((SELECT 'urn:address:basket:' || ba."BASKETID" || ':' || ba."UUID" FROM "BASKETADDRESS" ba WHERE b."SHIPFROMADDRESSURN" = ba."UUID"), b."SHIPFROMADDRESSURN"),
			NVL((SELECT 'urn:address:basket:' || ba."BASKETID" || ':' || ba."UUID" FROM "BASKETADDRESS" ba WHERE b."SERVICETOADDRESSURN" = ba."UUID"), b."SERVICETOADDRESSURN"),
			NVL((SELECT 'urn:address:basket:' || ba."BASKETID" || ':' || ba."UUID" FROM "BASKETADDRESS" ba WHERE b."INSTALLTOADDRESSURN" = ba."UUID"), b."INSTALLTOADDRESSURN")
		FROM DUAL
	);


	-- PRODUCTLINEITEM
	UPDATE "PRODUCTLINEITEM" p SET (p."SHIPTOADDRESSURN", p."SHIPFROMADDRESSURN") = (
		SELECT
			NVL((SELECT 'urn:address:basket:' || ba."BASKETID" || ':' || ba."UUID" FROM "BASKETADDRESS" ba WHERE p."SHIPTOADDRESSURN" = ba."UUID"), p."SHIPTOADDRESSURN"),
			NVL((SELECT 'urn:address:basket:' || ba."BASKETID" || ':' || ba."UUID" FROM "BASKETADDRESS" ba WHERE p."SHIPFROMADDRESSURN" = ba."UUID"), p."SHIPFROMADDRESSURN")
		FROM DUAL
	)
	WHERE EXISTS (
		SELECT 1
		FROM "BASKET" b
		WHERE p."LINEITEMCTNRID" = b."UUID"
	);


	-- SERVICELINEITEM
	UPDATE "SERVICELINEITEM" s SET s."SHIPTOADDRESSURN" = NVL((SELECT 'urn:address:basket:' || ba."BASKETID" || ':' || ba."UUID" FROM "BASKETADDRESS" ba WHERE s."SHIPTOADDRESSURN" = ba."UUID"), s."SHIPTOADDRESSURN")
	WHERE EXISTS (
		SELECT 1
		FROM "BASKET" b
		WHERE s."LINEITEMCTNRID" = b."UUID"
	);

	COMMIT;

END;
